// Fonction qui parcourt la chaîne et ajoute un caractère à result par une valeur aléatoire dans le tableau 'leetSpeak'
function transLeet($string, $leetSpeakAlphabet): string
{

    $result = '';

    for($i = 0; $i < strlen($string); $i++){
        $char = strtolower($string[$i]);
        if(isset($leetSpeakAlphabet[$char])){
            $result .= $leetSpeakAlphabet[$char][array_rand($leetSpeakAlphabet[$char])];
        }
    }
    return $result;

}

